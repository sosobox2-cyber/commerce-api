package com.cware.netshopping.paqeen.domain;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown =true)
public class Category {
	private String id;
	private String title;
	private String parentId;
	private String iconUrl;
	private MultiResolutionIcon multiResolutionIcon;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getParentId() {
		return parentId;
	}
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	public String getIconUrl() {
		return iconUrl;
	}
	public void setIconUrl(String iconUrl) {
		this.iconUrl = iconUrl;
	}
	public MultiResolutionIcon getMultiResolutionIcon() {
		return multiResolutionIcon;
	}
	public void setMultiResolutionIcon(MultiResolutionIcon multiResolutionIcon) {
		this.multiResolutionIcon = multiResolutionIcon;
	}
}
